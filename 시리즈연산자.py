import pandas as pd

# 1. 기본 데이터프레임 생성 (이름, 국어, 영어)
df = pd.DataFrame({
    '이름': ['홍길동', '이순신', '임꺽정'],
    '국어': [80, 95, 70],
    '영어': [90, 85, 75]
})

print("--- 연산 전 기본 데이터프레임 ---")
print(df)
print("\n" + "="*30 + "\n")

# 2. 시리즈 간의 연산식으로 새로운 시리즈 생성 및 추가
# '국어' 시리즈와 '영어' 시리즈를 더해 '총점' 시리즈를 만듭니다.
df['총점'] = df['국어'] + df['영어']

# '총점' 시리즈를 2로 나누어 '평균' 시리즈를 만듭니다.
df['평균'] = df['총점'] / 2

print("--- 연산식 적용 후 최종 데이터프레임 ---")
print(df)
